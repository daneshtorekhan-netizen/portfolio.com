const descriptionBlocks = document.querySelectorAll("[data-description]");
const behanceLinks = document.querySelectorAll(
  ".project-image-link, .project-title-link, .project-link",
);

descriptionBlocks.forEach((block) => {
  const text = block.querySelector(".project-description");
  const collapsedLines = 3;
  let toggle = null;

  const createToggle = () => {
    if (toggle) {
      return;
    }

    toggle = document.createElement("button");
    toggle.className = "description-toggle";
    toggle.type = "button";
    toggle.setAttribute("aria-expanded", "false");
    toggle.textContent = "Read more";

    toggle.addEventListener("click", () => {
      const isExpanded = toggle.getAttribute("aria-expanded") === "true";

      toggle.setAttribute("aria-expanded", String(!isExpanded));
      text.style.maxHeight = isExpanded
        ? `${text.dataset.collapsedHeight}px`
        : `${text.scrollHeight}px`;
      toggle.textContent = isExpanded ? "Read more" : "Show less";
    });

    block.append(toggle);
  };

  const updateDescription = () => {
    const wasExpanded = toggle?.getAttribute("aria-expanded") === "true";
    const lineHeight = Number.parseFloat(getComputedStyle(text).lineHeight);
    const collapsedHeight = lineHeight * collapsedLines;

    text.style.maxHeight = "";
    text.dataset.collapsedHeight = String(collapsedHeight);

    if (text.scrollHeight <= collapsedHeight + 1) {
      toggle?.remove();
      toggle = null;
      return;
    }

    createToggle();

    toggle.setAttribute("aria-expanded", String(wasExpanded));
    toggle.textContent = wasExpanded ? "Show less" : "Read more";
    text.style.maxHeight = wasExpanded
      ? `${text.scrollHeight}px`
      : `${collapsedHeight}px`;
  };

  updateDescription();
  window.addEventListener("resize", updateDescription);
});

